public class PersonModel
{
    private String name;
    private int years;

    public String getName()
    {
        return name;
    }
    public void setName(String setName)
    {
        this.name = setName;
    }
    public int getYears()
    {
        return years;
    }
    public void setYears(int setYears)
    {
        this.years = setYears;
    }

}
